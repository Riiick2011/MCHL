class Head(nn.Module):
    # YOLOv8 Detect head for detection models
    shape = None

    def __init__(self, chout=256, chin=[256, 512, 512]):  # detection layer
        super().__init__()
        self.nl = len(chin)  # number of detection layers
        self.chin = chin
        self.chout = chout
        self.cv = nn.ModuleList(Conv(x, self.chout, 1) for x in self.chin)

    def forward(self, x):
        shape = x[0].shape  # BCHW
        pyramid_feats = [self.cv[i](x[i]) for i in range(self.nl)]
        return pyramid_feats
